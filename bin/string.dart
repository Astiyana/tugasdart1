void main() {
  String firstName = "ASTI";

  String lastName = "YANA";

  print(firstName);
  print(lastName);


var fullName = "$firstName ${lastName}";

print(fullName);

// kode karakter backslash
var text = "this is \'dart\' \$cool";
print(text);

// menggabungkan string
var name1 = firstName + lastName;
var name2 = "ASTI" "YANA" "RAHAYU";

print(name1);
print(name2);

// multiline string
var longString = "";
print(longString);
}
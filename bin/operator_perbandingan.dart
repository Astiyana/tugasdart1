void main() {
  int a = 20;
  int b = 5;
  int c = 4;
  int d = 2;
  double e = 18.5;
  double f = 20.5;

  //menggunakan sama dengan (=)
  print("apakah nilai a sama dengan nilai b? ${a == b}");

  //menggunakan tidak sama dengan (!=)
  print("apakah nilai a tidak sama dengan nilai c? ${a != c}");

  //menggunakan lebih dari (>)
  print(a > d);

  //menggunakan kurang dari (<)
  print (b < c);

  //lebih dari atau sama dengan
  print (e <= f);

  //kurang dari atau sama dengan
  print(e >= f);
}